package ru.globus.model.enums;

import lombok.Getter;
import lombok.RequiredArgsConstructor;

/**
 * Перечисление кодов валют, поддерживаемых системой.
 * Используется для указания валюты банковского счёта и при работе с курсами валют.
 */
@Getter
@RequiredArgsConstructor
public enum CurrencyCode {

    /** Австралийский доллар. */
    AUD("Австралийский доллар"),

    /** Фунт стерлингов. */
    GBP("Фунт стерлингов"),

    /** Белорусский рубль. */
    BYR("Белорусский рубль"),

    /** Датская крона. */
    DKK("Датская крона"),

    /** Доллар США. */
    USD("Доллар США"),

    /** Евро. */
    EUR("Евро"),

    /** Исландская крона. */
    ISK("Исландская крона"),

    /** Тенге. */
    KZT("Тенге"),

    /** Канадский доллар. */
    CAD("Канадский доллар"),

    /** Норвежская крона. */
    NOK("Норвежская крона"),

    /** Российский рубль. */
    RUB("Российский рубль"),

    /** СДР — специальные права заимствования. */
    XDR("СДР (специальные права заимствования)"),

    /** Сингапурский доллар. */
    SGD("Сингапурский доллар"),

    /** Турецкая лира. */
    TRL("Турецкая лира"),

    /** Гривна. */
    UAH("Гривна"),

    /** Шведская крона. */
    SEK("Шведская крона"),

    /** Швейцарский франк. */
    CHF("Швейцарский франк"),

    /** Японская иена. */
    JPY("Японская иена");

    /** Полное русскоязычное название валюты. */
    private final String displayName;

    /**
     * Возвращает строковое представление валюты в формате: "USD (Доллар США)".
     */
    @Override
    public String toString() {
        return name() + " (" + displayName + ")";
    }

    /**
     * Находит элемент перечисления по строковому коду валюты.
     *
     * @param code строковый код валюты, например "USD" или "eur"
     * @return соответствующий элемент перечисления {@link CurrencyCode}
     * @throws IllegalArgumentException если передан неизвестный код
     */
    public static CurrencyCode fromCode(String code) {
        for (CurrencyCode c : values()) {
            if (c.name().equalsIgnoreCase(code)) {
                return c;
            }
        }
        throw new IllegalArgumentException("Неизвестный код валюты: " + code);
    }
}
